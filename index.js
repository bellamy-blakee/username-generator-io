function getRandomElement(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}
function generateUsernames(keyword, options = {}) {
  const usernames = [];

  for (let i = 0; i < 10; i++) {
    // Use the provided style, or randomly pick one if not provided
    const chosenStyle =
      options.style || getRandomElement(Object.keys(stylesData.styles));

    if (!stylesData.styles[chosenStyle]) {
      throw new Error(`Style "${chosenStyle}" not found.`);
    }

    const style = stylesData.styles[chosenStyle];
    const pattern = getRandomElement(patterns);
    const replacements = {
      "{keyword}": keyword,
      "{noun}": getRandomElement(style.nouns),
      "{verb}": getRandomElement(style.verbs),
      "{adjective}": getRandomElement(style.adjectives),
      "{number}": options.luckyNumber || Math.floor(Math.random() * 1000), // Use provided lucky number, or generate a random one
      "{prefix}": options.prefix || getRandomElement(stylesData.prefixes), // Use provided prefix, or get a random one
      "{suffix}": options.suffix || "", // Use provided suffix, or nothing if not provided
    };

    let username = pattern;
    for (const [key, value] of Object.entries(replacements)) {
      username = username.replace(new RegExp(key, "g"), value);
    }

    usernames.push(username);
  }

  return usernames;
}
